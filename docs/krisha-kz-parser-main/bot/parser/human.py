async def delay_between_listings() -> None:
    await asyncio.sleep(random.uniform(1.0, 3.0))


async def delay_between_pages() -> None:
    await asyncio.sleep(random.uniform(2.0, 5.0))


def build_session() -> requests.Session:
    """Build a browser-like requests session. X-Requested-With is NOT set globally —
    it must be added per-request only where needed (ajaxPhones)."""
    session = requests.Session()
    session.headers.update({
        "User-Agent": random.choice(_USER_AGENTS),
        "Accept-Language": "ru-RU,ru;q=0.9,en-US;q=0.8,en;q=0.7",
        "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,image/avif,image/webp,*/*;q=0.8",
        "Accept-Encoding": "gzip, deflate",
        "Connection": "keep-alive",
    })
    return session
